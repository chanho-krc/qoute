function generateStats(quotes) {
  const stats = {
    total: quotes.length,
    byCategory: {},
    generatedAt: new Date().toISOString(),
    version: "2.0.0"
  };
  
  quotes.forEach(quote => {
    if (!stats.byCategory[quote.category]) {
      stats.byCategory[quote.category] = 0;
    }
    stats.byCategory[quote.category]++;
  });
  
  return stats;
}

